// Search functionality
function handleSearch(query) {
    const searchTerm = query.toLowerCase().trim();
    
    if (searchTerm === '') {
        filteredMembers = [...squadMembers];
    } else {
        filteredMembers = squadMembers.filter(member => 
            member.name.toLowerCase().includes(searchTerm) ||
            member.bio.toLowerCase().includes(searchTerm)
        );
    }
    
    renderSquadCards(filteredMembers);
}

// Open modal
function openModal(member) {
    currentMember = member;
    
    document.getElementById('modalName').textContent = member.name;
    document.getElementById('modalBio').textContent = member.bio;
    document.getElementById('modalProjects').textContent = member.projects;
    document.getElementById('modalEmail').textContent = member.email;
    
    const githubLink = document.getElementById('modalGithub');
    const linkedinLink = document.getElementById('modalLinkedin');
    const emailLink = document.getElementById('modalEmailLink');
    
    if (member.github) {
        githubLink.href = member.github;
        githubLink.style.display = 'flex';
    } else {
        githubLink.style.display = 'none';
    }
    
    if (member.linkedin) {
        linkedinLink.href = member.linkedin;
        linkedinLink.style.display = 'flex';
    } else {
        linkedinLink.style.display = 'none';
    }
    
    emailLink.href = `mailto:${member.email}`;
    
    modalOverlay.classList.add('active');
    document.body.style.overflow = 'hidden';
    const modalAvatar = document.getElementById("modalAvatar");

modalAvatar.innerHTML = getAvatar(member)
  ? `<img src="${getAvatar(member)}">`
  : `<div class="fallback">${getInitials(member.name)}</div>`;
  const githubStats = document.getElementById("githubStats");

if (member.github) {
  const username = member.github.split("github.com/")[1];
  githubStats.src = `https://github-readme-stats.vercel.app/api?username=${username}&show_icons=true&theme=dark`;
  githubStats.style.display = "block";
} else {
  githubStats.style.display = "none";
}
}

// Close modal
function closeModal() {
    modalOverlay.classList.remove('active');
    document.body.style.overflow = '';
    currentMember = null;
}

// Setup event listeners
function setupEventListeners() {
    // Search
    searchInput.addEventListener('input', (e) => {
        handleSearch(e.target.value);
    });
    
    // Modal close
    modalClose.addEventListener('click', closeModal);
    
    modalOverlay.addEventListener('click', (e) => {
        if (e.target === modalOverlay) {
            closeModal();
        }
    });
    
    // Keyboard navigation
    document.addEventListener('keydown', (e) => {
        if (e.key === 'Escape' && modalOverlay.classList.contains('active')) {
            closeModal();
        }
    });
}

// Initialize on DOM load
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', init);
} else {
    init();
}

function animateCounters() {
  const counters = document.querySelectorAll(".stat-value");

  counters.forEach(counter => {
    const target = +counter.getAttribute("data-target");
    let count = 0;

    const update = () => {
      const increment = target / 60;

      if (count < target) {
        count += increment;
        counter.innerText = Math.floor(count);
        requestAnimationFrame(update);
      } else {
        counter.innerText = target >= 1000 ? target / 1000 + "K+" : target + "+";
      }
    };

    update();
  });
}

const observer = new IntersectionObserver(entries => {
  if (entries[0].isIntersecting) {
    animateCounters();
  }
});

observer.observe(document.querySelector(".stats-grid"));